#include<stdio.h>

void main(){

    int a = 1, b = 2;
    // verdadeiro == 1
    // falso == 0

    //menor que
    printf("a < b : %i\n", a < b);
    //maior que
    printf("a > b : %i\n", a > b);
    //menor ou igual
    printf("a <= b : %i\n", a <= b);
    //maior ou igual
    printf("a >= b: %i\n", a >= b);
    //equivalente
    printf("a == b: %i\n", a == b);
    //diferente
    printf("a != b: %i\n", a != b);
    
}